fun main() {
    //var dapat diubah nilainya
    var nama: String = "Ikhsan"
    nama = "Rahmad Noor Ikhsan"
    println(nama)
    //+ dapat menggabungkan 2 variabel
    val firstword = "Dicoding "
    val lastword = "Academy"
    print(firstword+lastword)
    //akan berbeda jika "+" digunakan pada int akan dijumlahkan
    val valueA: Int = 10
    val valueB= 20
    print(valueA+valueB)
}

