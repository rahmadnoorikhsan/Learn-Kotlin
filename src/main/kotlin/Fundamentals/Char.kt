fun main() {
    //Char bisa didefinisikan dengan tanda kutip ('')
    val character = 'A'
    /*
    Char akan error jika memasukkan lebih dari 1 karakter
    val character = 'ABC'
     */

    //Char dapat melakukan operasi increment(++) dan Decrement (--)
    var vocal = 'A'

    println("vocal " + vocal++)
    println("vocal " + vocal++)
    println("vocal " + vocal++)
    println("vocal " + vocal--)
    println("vocal " + vocal--)
    println("vocal " + vocal--)
    println("vocal " + vocal++)
    println("vocal " + vocal--)
}